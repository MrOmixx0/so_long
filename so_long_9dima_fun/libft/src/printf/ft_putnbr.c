#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	count;

	count = 0;
	if (n == -2147483648)
		return (write (1, "-2147483648", 11), 11);
	if (n < 0)
	{
		n = -n;
		count += write (1, "-", 1);
	}
	if (n > 9)
		count += ft_putnbr(n / 10);
	return (count + ft_putchar((n % 10) + '0'));
}
