#include "libft.h"

int	ft_atoi(const char *nptr)
{
	size_t	i;
	size_t	sign;
	size_t	results;

	i = 0;
	sign = 1;
	results = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		results = results * 10 + (nptr[i] - 48);
		i++;
	}
	return (results * sign);
}
